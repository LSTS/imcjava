package pt.lsts.imc;

/**
 *  IMC Message Desired Speed (402)<br/>
 *  Desired Speed reference value for the control layer.<br/>
 */

@SuppressWarnings("unchecked")
public class DesiredSpeed extends ControlCommand {

	public enum SPEED_UNITS {
		METERS_PS(0),
		RPM(1),
		PERCENTAGE(2);

		protected long value;

		public long value() {
			return value;
		}

		SPEED_UNITS(long value) {
			this.value = value;
		}
	}

	public static final int ID_STATIC = 402;

	public DesiredSpeed() {
		super(ID_STATIC);
	}

	public DesiredSpeed(IMCMessage msg) {
		super(ID_STATIC);
		try{
			copyFrom(msg);
		}
		catch (Exception e) {
			e.printStackTrace();
		}
	}

	public DesiredSpeed(IMCDefinition defs) {
		super(defs, ID_STATIC);
	}

	public static DesiredSpeed create(Object... values) {
		DesiredSpeed m = new DesiredSpeed();
		for (int i = 0; i < values.length-1; i+= 2)
			m.setValue(values[i].toString(), values[i+1]);
		return m;
	}

	public static DesiredSpeed clone(IMCMessage msg) throws Exception {

		DesiredSpeed m = new DesiredSpeed();
		if (msg == null)
			return m;
		if(msg.definitions != m.definitions){
			msg = msg.cloneMessage();
			IMCUtil.updateMessage(msg, m.definitions);
		}
		else if (msg.getMgid()!=m.getMgid())
			throw new Exception("Argument "+msg.getAbbrev()+" is incompatible with message "+m.getAbbrev());

		m.getHeader().values.putAll(msg.getHeader().values);
		m.values.putAll(msg.values);
		return m;
	}

	public DesiredSpeed(double value, SPEED_UNITS speed_units) {
		super(ID_STATIC);
		setValue(value);
		setSpeedUnits(speed_units);
	}

	/**
	 *  @return Value - fp64_t
	 */
	public double getValue() {
		return getDouble("value");
	}

	/**
	 *  @param value Value
	 */
	public DesiredSpeed setValue(double value) {
		values.put("value", value);
		return this;
	}

	/**
	 *  @return Speed Units (enumerated) - uint8_t
	 */
	public SPEED_UNITS getSpeedUnits() {
		try {
			SPEED_UNITS o = SPEED_UNITS.valueOf(getMessageType().getFieldPossibleValues("speed_units").get(getLong("speed_units")));
			return o;
		}
		catch (Exception e) {
			return null;
		}
	}

	public String getSpeedUnitsStr() {
		return getString("speed_units");
	}

	public short getSpeedUnitsVal() {
		return (short) getInteger("speed_units");
	}

	/**
	 *  @param speed_units Speed Units (enumerated)
	 */
	public DesiredSpeed setSpeedUnits(SPEED_UNITS speed_units) {
		values.put("speed_units", speed_units.value());
		return this;
	}

	/**
	 *  @param speed_units Speed Units (as a String)
	 */
	public DesiredSpeed setSpeedUnitsStr(String speed_units) {
		setValue("speed_units", speed_units);
		return this;
	}

	/**
	 *  @param speed_units Speed Units (integer value)
	 */
	public DesiredSpeed setSpeedUnitsVal(short speed_units) {
		setValue("speed_units", speed_units);
		return this;
	}

}
